package lk.it.uom;

public class CalculatorLogic {
    public static int add(int x, int y){
        return x+y;
    }

    public static int substraction(int x, int y){
        return x-y;
    }

    public static int division(int x, int y){
        return x/y;
    }

    public static int multiplication(int x, int y){
       return x*y;
    }
}
